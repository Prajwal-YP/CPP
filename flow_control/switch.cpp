#include<iostream>

int main()
{
    int n=5;

    switch(n)
    {
        case 1:{
            std::cout<<"ONE";
        }
        break;
        
        case 2:{
            std::cout<<"TWO";
        }
        break;
        case 3:{
            std::cout<<"THREE";
        }
        break;
        case 4:{
            std::cout<<"FOUR";
        }
        break;
        case 5:{
            std::cout<<"FIVE";
        }
        break;
        case 6:{
            std::cout<<"SIX";
        }
        break;
    }

    return 0;
}